//Write a program to check if the letter 'e' is present in the word 'Umbrella'. (Use Method charAt());
package caharAt;

public class main {
    public static void main(String[] args) {
        String s = "Umbrella";
        if (s.charAt(4) == 'e') {
            System.out.println("Present");
        } else {
            System.out.println("Not Present");
        }

    }
}
